-- universities: 大学マスタ（購入ページ・Stripe 検証用）
-- 医学部を置く国公私立 81 校（文部科学省の区分に沿った都道府県・地方・設置区分）
-- 既存データを入れ直す場合: 下記 DELETE のあと INSERT を実行
-- Supabase Dashboard → SQL Editor で実行可

create table if not exists public.universities (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  prefecture text not null,
  region text not null,
  ownership text not null,
  created_at timestamptz not null default now(),
  unique (name)
);

comment on table public.universities is '大学マスタ（閲覧権購入・表示用）';
comment on column public.universities.name is '大学名（Stripe metadata university_name と一致させる）';
comment on column public.universities.prefecture is '都道府県';
comment on column public.universities.region is '地方: 北海道|東北|関東|中部|近畿|中国|四国|九州';
comment on column public.universities.ownership is '国立・公立・私立';

create index if not exists universities_region_idx on public.universities (region);
create index if not exists universities_prefecture_idx on public.universities (prefecture);

alter table public.universities enable row level security;

drop policy if exists "universities_select_all" on public.universities;
create policy "universities_select_all"
  on public.universities
  for select
  to anon, authenticated
  using (true);

